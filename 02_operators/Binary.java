public class Binary {
	public static void main(String[] args) {
		// Binary operators
		int A = 100;
		int B = 5;

		// Addition
		System.out.println("add: " + A + " + " + B + " = " + (A + B));

		// Subtraction
		System.out.println("sub: " + A + " - " + B + " = " + (A - B));

		// Multiplication
		System.out.println("mul: " + A + " * " + B + " = " + (A * B));

		// Division
		System.out.println("div: " + A + " / " + B + " = " + (A / B));

		// Modulo or Remainder
		System.out.println("mod: " + A + " % " + B + " = " + (A % B));
	}
}
