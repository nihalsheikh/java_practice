public class Variables {
	public static void main(String[] args) {
		// Declaring a variable
		int a = 10;
		int b = 5;
		System.out.println(a);
		System.out.println(b);

		String name = "John Doe";
		System.out.println(name);

		// Changing value of an existing variable
		a = 100;
		System.out.println(a);
	}
}
